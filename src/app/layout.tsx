import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.scss";
import Header from "./components/header";
import { Footer, IFooterProps } from "./components/footer";
import { FC } from "react";
import getLayoutData from "@api/layout";
import { AntdRegistry } from "@ant-design/nextjs-registry";
import ThemeContextProvider from "@/stores/theme";
import UserAgentProvider from "@/stores/userAgent";
import styles from "./page.module.scss";
import Script from "next/script";
import { getIsMobile } from "@/utils";
import { headers } from "next/headers";

const inter = Inter({ subsets: ["latin"] });

export interface ILayoutProps {
  footerData: IFooterProps;
}

export const metadata: Metadata = {
  title:
    (getIsMobile(headers().get("user-agent")) ? "mobile" : "pc") +
    " Create Next App",
  description: "Generated by create next app",
};

const RootLayout: FC<{ children: JSX.Element }> = async ({ children }) => {
  const data = await getLayoutData();
  const isMobile = getIsMobile(headers().get("user-agent"));

  return (
    <html lang="en">
      <ThemeContextProvider>
        <UserAgentProvider>
          <body className={inter.className}>
            <Header isMobile={isMobile} />
            <AntdRegistry>
              <main className={styles.main}>{children}</main>
            </AntdRegistry>
            <Footer {...data} />
            <Script id="theme-script" strategy="beforeInteractive">
              {`const item = localStorage.getItem('theme') || 'light';
          localStorage.setItem('theme', item);
          document.getElementsByTagName('html')[0].dataset.theme = item;`}
            </Script>
          </body>
        </UserAgentProvider>
      </ThemeContextProvider>
    </html>
  );
};

export default RootLayout;
